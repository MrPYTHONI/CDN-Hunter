# Function to expand CIDR range
expand_cidr() {
  local range=$1
  IFS=/ read ip mask <<< "$range"
  IFS=. read -r i1 i2 i3 i4 <<< "$ip"
  local ip_start=$(( (i1 << 24) + (i2 << 16) + (i3 << 8) + i4 ))
  local ip_end=$(( ip_start + (1 << (32 - mask)) - 1 ))

  for ((ip=ip_start; ip<=ip_end; ip++)); do
    printf "%d.%d.%d.%d\n" \
      $(( (ip >> 24) & 255 )) \
      $(( (ip >> 16) & 255 )) \
      $(( (ip >> 8) & 255 )) \
      $(( ip & 255 ))
  done
}

